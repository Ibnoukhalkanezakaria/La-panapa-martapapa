#include "../push_swap.h"

void	check_one(t_stack *box, char *r)
{
	int	i;

	i = 0;
	while (r[i])
	{
		if (r[i] != '+' && r[i] != '-')
		{
			if (!(r[i] >= '0' && r[i] <= '9'))
				box->error = 1;
		}
		i++;
	}
}
