#include "../push_swap.h"

void	inside_number(t_stack *box, char *r)
{
	int	i;

	i = 0;
	while (r[i])
	{
		if (r[i] == '+' || r[i] == '-')
		{
			if (i > 0)
				box->error = 1;
		}
		i++;
	}
}
